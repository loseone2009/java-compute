package org.bm.analysis.exception;

public class MathematicalAnalysisException extends Exception {
    private static final long serialVersionUID = 347948960180024146L;

    public MathematicalAnalysisException() {
        super();
    }

    public MathematicalAnalysisException(String message, Throwable cause) {
        super(message, cause);
    }

    public MathematicalAnalysisException(String message) {
        super(message);
    }

    public MathematicalAnalysisException(Throwable cause) {
        super(cause);
    }

}
